class FormatUtils {
  FormatUtils._();

  /// Formats a number to a compact string (e.g. 1200 → "1.2K")
  static String formatCount(int count) {
    if (count >= 1000000) {
      return '${(count / 1000000).toStringAsFixed(1)}M';
    } else if (count >= 1000) {
      return '${(count / 1000).toStringAsFixed(1)}K';
    }
    return count.toString();
  }
}
